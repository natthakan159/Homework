package com.masterclass.homework;

public class FouthHomeWorkSwitchCase {
    public static void main(String[] args) {
        char nameChar = 'A';
        switch (nameChar){
            case 'A':
                System.out.println("Char is " + nameChar);
                break;
            case 'B':
                System.out.println("Char is " + nameChar);
                break;
            case 'C':
                System.out.println("Char is " + nameChar);
                break;
            case 'D':
                System.out.println("Char is " + nameChar);
                break;
            case 'E':
                System.out.println("Char is " + nameChar);
                break;
            default:
                System.out.println("Char is was not A, B, C, D or E");
                break;
        }
    }
}
